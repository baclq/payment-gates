function validateImage(fileInput){
    var filePath = fileInput.value;
    var allowedExtensions = /(\.jpg|\.jpeg|\.png|\.gif)$/i;
    if(!allowedExtensions.exec(filePath)){
        fileInput.value = '';
        return false;
    }
    return true;
}
